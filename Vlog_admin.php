                    <strong>Life seems to have been so chaotic lately that I don’t seem to know whether I am coming
                    or going. So much so that I almost missed an edition of South East
                    Farmer, so please accept my apologies if this article appears a
                    little rushed.</strong> <br> <br> 
                    <i> - by Anita Head </i><br>
                    <br> The Christmas tree is now in residence, the children have finished school without any
                    disruption and plans for the festive season are well under way.
                    Christmas shopping is complete (a huge sigh of relief is exhaled
                    from certain family members) and when all the work has been done we
                    shall eat, drink and be merry – or rather fall asleep and start
                    again the following day. <br><br>
                    Having sat down to eat a hastily prepared
                    supper, it was family discussion time at the dinner table… 
                    <br><br>
                    Modern machinery was the topic tonight. What excitement; is it better to
                    have modern machinery that seem to be rather expensive to repair or
                    older machinery that you can repair yourselves? We have a relatively
                    new telehandler that seems to be a “Friday afternoon” job, small
                    problems that continue to annoy us on a daily basis. If you total up
                    the cost of the machine and the cost of the ongoing issues it
                    becomes a little more than a never-ending empty bank account. 
                    <br><br>
                    The problem that we encounter is that the machine is in use 365 days a
                    year. On a cold, damp morning it is essential that the machine
                    starts and performs well. The ever-faithful Ford tractor that Fergus
                    has lovingly restored is easy to fix and always seems to start come
                    rain or shine. It’s not the most luxurious or comfortable machine,
                    but these are some of the sacrifices to be made.<br><br>
                    Tom Pemberton Farm Life seems to be the latest obsession in our house. His YouTube
                    vlogs (online videos) seem to be played the instant they appear. He
                    certainly has a way, and I must confess to having watched a couple.
                    I’m undecided as to whether he is a good influence or not. I wonder
                    how he manages to get away with some of the things he does;
                    personally I think the “Red Shepherdess” has a lot to do with the
                    older members of the family watching it.<br><br>
                    I have unsuccessfully been
                    trying to purchase some of his “merch”. The minute it goes live on
                    the internet it seems to have sold out, so clearly the boys are not
                    the only ones to be obsessed. The farm shop and clothing must be
                    bringing in huge amounts of revenue.<br><br>
                    The annual farm subsides will
                    decrease by at least 50% BY 2024. The BPS will start being reduced
                    from 2021. Businesses that receive £30,000 or less will see their
                    subsidy fall by 5% in 2021, followed by a gradual increase to 50% in
                    2024. Farmers who receive more than this will see their BPS fall at
                    an even higher rate. The Government has launched its path to
                    sustainable farming. I believe the changes will be brought in over
                    the next seven years. The Environmental Land Management scheme
                    (ELMS) will be introduced to give an incentive for sustainable
                    farming processes whilst boosting the environment.<br><br>
                    Level 1 of ELMS
                    will be the sustainable farming incentive that will aid farm
                    husbandry and commend soil improvement and pest management. Level 2
                    – Natural England – will pay for creating, managing and restoring
                    wildlife habitats etc and Level 3 will be landscape recovery through
                    projects that will include large-scale woodland creations. The
                    Government has promised that they will simplify existing schemes and
                    their application processes; cutting red tape would be a start. <br><br>
                    A
                    new farming investment fund to enable businesses to invest in the
                    future in things such as equipment, technology and infrastructure
                    will all form part of it. Farming will not have seen a change like
                    this in over 70 years. We shall see how it all pans out. If the
                    Government has anything to do with it, I wouldn’t hold out much
                    hope. Let us hope we can all live off bark, butterflies and beetles.<br><br>

                    All that remains is for me to wish you all an incredibly happy
                    Christmas and a prosperous new year. Stay safe in 2021.<br><br>
                    
